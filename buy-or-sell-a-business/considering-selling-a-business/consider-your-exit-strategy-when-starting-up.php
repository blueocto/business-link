		<h1>Consider your exit strategy when starting up</h1>
		<h2>Introduction</h2>
		<span><img src="../../Starting_a_Business_images/SU0360.gif" width="78" height="118"  alt="" /></span>
		<p>When you're setting up your business it's essential to think about how you'll ultimately end your involvement with it.</p>
		<p>A well thought-out exit strategy can help you to maximise the value you get from your business, successfully market your business to potential buyers or investors&nbsp;and ensure you end your involvement with as little disruption to the business as possible.</p>
		<p>Regardless of whether your exit occurs&nbsp;to a planned&nbsp;schedule or you are forced to make a move for&nbsp;unexpected reasons, the decisions you make when setting up can affect how easy it is for you to eventually exit your business.</p>
		<p>This guide provides an overview of how some decisions can affect your ability to exit the business successfully, and shows you how to prepare and manage your business to maximise its value. It also covers the different exit options available and outlines their advantages and disadvantages.</p>

