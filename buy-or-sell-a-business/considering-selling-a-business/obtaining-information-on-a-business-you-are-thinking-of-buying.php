		<h1>Obtaining information on a business you are thinking of buying</h1>
		
		<h2>Introduction</h2>
		<span><img src="../../Starting_a_Business_images/info_on_business.gif" width="94" height="62"  alt="" /></span>
		<p>Buying an established business is a good option for some aspiring entrepreneurs. Much of the groundwork for success may have already been done by the existing owners.</p>
		<p>Before making an offer there are some important things to investigate. Initially you&nbsp;may only have access to the business' sales memorandum. Sellers will usually ask you to sign a confidentiality undertaking or non-disclosure agreement before you can access sensitive or detailed information. They will usually also ask for details about you (via a CV) and evidence of your ability to fund a purchase.</p>
		<p>After an offer is made and accepted, due diligence is carried out. This is where the buyer looks at the business in detail, including its finances, its employees, outstanding litigation, major contracts, IT and other technology.</p>
		<p>It may sound complex, but professionals such as accountants, solicitors, chartered surveyors, business transfer agents, business brokers and corporate financiers can help. You can also do some of the research yourself.</p>

