		<h1>Understanding contracts when buying or selling a business</h1>
		
		<h2>Introduction</h2>
		<span><img src="../../Customers_and_Suppliers_images/contracts.gif" width="94" height="62"  alt="" /></span>
		<p>There are several stages to buying or selling a business. They include valuing the business, getting tax advice, market research and marketing, preliminary offers, negotiating terms, heads of agreement, legal sale and purchase agreement, completing due diligence, and finally, completing the sale.</p>
		<p>Ensure each stage of the negotiation is documented in order to include all agreements and conditions in the final contract. Do this even if the business is small and the sale straightforward.</p>
		<p>Take&nbsp;expert advice from an accountant and a solicitor throughout the process to avoid costly mistakes and unexpected obligations. You can also seek advice from a tax specialist to ensure that the deal you agree is tax efficient for you. Sellers should decide whether to use a business broker or corporate financier to handle the initial part of the process.</p>
		<p>This guide sets out the main agreements and contracts involved&nbsp;in the process of buying or selling a business, what they should cover, and where to seek advice.<br />
		</p>

