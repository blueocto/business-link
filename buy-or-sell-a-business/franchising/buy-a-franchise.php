			<h1>Buy a franchise</h1>
			<h2>Introduction</h2>
			<span><img src="/_img/SU0830.gif" width="94" height="62" alt="" /></span>
			<p>Taking on a franchise is an option worth considering for anyone who wants to <b>run a business</b> but doesn't have a specific business idea or prefers the security provided by an established concept.</p>
			<p>The right franchise can give you a head start. Instead of setting up a business from scratch, you <strong>use a proven business idea</strong>. Typically, you trade under the brand name of the business offering you the franchise, and they give you help and support.</p>
			<p>Successful franchises have a much lower failure rate than completely new businesses. However, you will still need to work hard to make the franchise a success and you may have to sacrifice some of your own business ideas to fit in with the franchisor's terms.</p>
			<p>This guide will help you decide whether franchising is for you. It shows how you can find the right franchise, and highlights the key issues you need to consider.</p>
