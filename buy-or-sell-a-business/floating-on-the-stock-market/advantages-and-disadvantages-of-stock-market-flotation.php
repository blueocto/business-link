		<h1>Floating on the stock market</h1>
		<h2>Advantages and disadvantages of stock market flotation</h2>
		<p>Even if your business is suited to flotation, it may not be the right choice for you. Being a public company can present a range of benefits to your business, but there are also issues that might require careful consideration.</p>
		<p>The <strong>benefits</strong> of stock market flotation could include:</p>
		<ul>
			<li> giving access to new capital to develop the business</li>
			<li> making it easier for you and other investors - including venture capitalists - to realise their investment</li>
			<li> allowing you to offer employees extra incentives by granting share options - this can encourage and motivate your employees to work towards long-term goals</li>
			<li> placing a value on your business</li>
			<li> increasing your public profile, and providing reassurance to your customers and suppliers</li>
			<li> allowing you to do business - eg acquisitions - by using quoted shares as currency</li>
			<li> creating a market for the company's shares</li>
		</ul>
		<p>However, you should also consider the following potential <strong>problems</strong>:</p>
		<ul>
			<li> <strong>Market fluctuations</strong> - your business may become vulnerable to market fluctuations beyond your control - including market sentiment, economic conditions or developments in your sector.</li>
			<li> <strong>Cost</strong> - the costs of flotation can be substantial and there are also ongoing costs of being a public company, such as higher professional fees.</li>
			<li> <strong>Responsibilities to shareholders</strong> - in return for their capital, you will have to consider shareholders' interests when running the company - which may differ from your own objectives.</li>
			<li><strong>The need for transparancy</strong> - public companies must comply with a wide range of additional regulatory requirements and meet accepted standards of corporate governance including transparancy, and needing to make announcements about new developments.</li>
			<li> <strong>Demands on the management team</strong> - managers could be distracted from running the business during the flotation process and through needing to deal with investors afterwards.</li>
			<li> <strong>Investor relations</strong> - to maximise the benefits of being a public company and attract further investor interest in shares, you will need to keep investors informed.</li>
			<li> <strong>Employees may become demotivated</strong> - if shares are only offered to selected employees, there could be resentment. Shareholding employees could feel that there is little left to work for if they are sitting on valuable shares.</li>
		</ul>
