			<h1>Our use of cookies</h1>
			<h2>How cookies are used on this site </h2>
			<p>When we provide services, we want to make them easy, useful and reliable. Where services are delivered on the internet, this sometimes involves placing small amounts of information on your device, for example your computer or mobile phone. These include small filesknown as &quot;cookies&quot;. We do not use these to identify you personally, or to hold any personal data.</p>
			<p>These pieces of information are used to improve services for you through, for example:</p>
			<ul>
				<li>enabling a service to recognise your device so you don't have to give the same information several times during one task</li>
				<li>recognising that you may already have provided a username and password so that you don't need to keep repeating these for every web page requested</li>
				<li>measuring how many people are using services so they can be improved and there is enough capacity to ensure they are fast</li>
			</ul>
			<p>You can easily manage these small files yourself if you know how. You can <a href="http://www.direct.gov.uk/managingcookies" target="bgExternalonlinebusinesslinkgovuk" onclick="p=window.open('http://www.direct.gov.uk/managingcookies', 'bgExternalonlinebusinesslinkgovuk');p.focus();" title="What cookies are, why websites use them and how you can manage them" >find guidance on managing cookies on the Directgov website<span> - Opens in a new window</span></a>.</p>
			<h3>Cookies used by this service</h3>
			<p>There are two types of cookie:</p>
			<p><strong>Persistent cookies</strong> remain on your computer for a specified time and so are saved to your computer's hard disk. We use persistent cookies to:</p>
			<ul>
				<li>identify that a registered user has returned</li>
				<li>securely remember your login User ID - if you have asked us to do so</li>
				<li>understand how you use our site and obtain information on website usage - as described above</li>
			</ul>
			<p><strong>Session cookies</strong> are deleted when you close your browser and are usually not saved to your computer's hard disk. We use session cookies to:</p>
			<ul>
				<li>hold some types of non-private information you provide while using an interactive tool on the website</li>
				<li>identify that you have logged in to the site</li>
				<li>hold your search criteria while you are carrying out a search of the website</li>
				<li>collect information on website usage - as described above</li>
			</ul>
