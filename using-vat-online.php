				<h1>Using VAT Online</h1>
				<p>VAT Online is provided by HM Revenue &amp; Customs (HMRC) in partnership with Business Link.</p>
				<p>This page links you to VAT Online on the HMRC website.</p>
				<div><a href="https://online.hmrc.gov.uk/linkto?hmrctxnid=VAT_Trader_AAG&amp;xgovk3w=bl1000&amp;xgovf0p=|xgovs9k=hmrc|xgovr3h=VAT_Trader_AAG|xgovc8h=1000|xgovk3w=bl1000|&amp;xgovd2v=en&amp;xgovj6d=96641c8a5c958801d93f565fdb543c269713ad2e">Log in to VAT Online</a> </div>
				<br />
				<br />
				<br />
				<p>VAT Online requires registration. If you have not already done so, <a href="https://online.hmrc.gov.uk/linkto?hmrctxnid=hmrcOS&amp;xgovk3w=bl1000&amp;xgovf0p=|xgovs9k=hmrc|xgovr3h=hmrcOS|xgovc8h=1000|xgovk3w=bl1000|&amp;xgovd2v=en&amp;xgovj6d=db73bad8b8ccae0c3719ef70bcf7ebdb41dd90e0">register with HMRC for VAT Online</a>.</p>
				<p>If you are a tax adviser or accountant please use <a href="detail242e.html?itemId=1083089607&amp;type=PIP" title="Access VAT Online services for agents from HMRC">VAT Online for agents</a>.</p>
				<h3>Service availability</h3>
				<p><a href="http://customs.hmrc.gov.uk/channelsPortalWebApp/channelsPortalWebApp.portal?_nfpb=true&amp;_pageLabel=pageOnlineServices_ShowContent&amp;propertyType=document&amp;id=HMCE_MIG_009921#P155_10104" target="bgExternalonlinebusinesslinkgovuk"  title="Information on the current availability of HMRC's various web-based services">Check the availability of HMRC's Online Services<span> - Opens in a new window</span></a>.</p>
				<h3>Service issues</h3>
				<p><a href="http://customs.hmrc.gov.uk/channelsPortalWebApp/channelsPortalWebApp.portal?_nfpb=true&amp;_pageLabel=pageOnlineServices_ShowContent&amp;id=HMCE_PROD1_028665&amp;propertyType=document" target="bgExternalonlinebusinesslinkgovuk"  title="Current service issues for VAT Online">Check for any issues with HMRC's Online Services<span> - Opens in a new window</span></a>.</p>
				<h3>Related guidance</h3>
				<p>See our section on <a href="layer7a87.html?topicId=1083126673" title="How VAT works, how it is charged, the different rates, who needs to be registered and links to further information">VAT</a>.</p>
				<h3>Disclaimer</h3>
				<p>Please note that HMRC's terms and conditions will apply when using VAT Online.</p>
