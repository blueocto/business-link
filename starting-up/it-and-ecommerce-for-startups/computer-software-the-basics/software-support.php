		<h1>Computer software: the basics</h1>
		<h2>Software support</h2>
		<p>An important factor in choosing software is the availability of <strong>technical support</strong>.</p>
		<p>The advantages of having an expert available to handle your queries are obvious but, it's wise to check where the software support is based. If the experts are based in the US, for example, calling them will be expensive and the time difference may mean that they're not available all day.</p>
		<p>The software seller may have an <strong>online discussion forum</strong> where customers can ask questions about, and provide answers to, problems. Emailing the software seller's support team directly may also be an option, although they may take time to reply. Some software vendors also provide support via instant messaging with a member of their support team, so you can receive real-time help whenever you have questions or problems.</p>
		<p>How much the support <strong>costs</strong> is important. If software is faulty, do you still have to pay to have any problems fixed? You might have to, particularly if support is on a premium-rate phone line.</p>
		<p>You may get a period of free technical support while you install your software. However, software support is usually charged for after a certain period of time or after a certain number of queries. Check the cost carefully.</p>
		<p>More importantly, suppliers may stop offering support for older versions of software. This is a very important issue when updates are needed to keep software secure.</p>
		<p>If your business depends on your software, fixing problems quickly may be vital. If this is the case, you will need a maintenance contract, but always read the small print. See our guide on how to <a href="layer1a6f.html?topicId=1074003275" title="The importance of choosing the right IT supplier and developing an effective working relationship with them">choose and manage your IT supplier</a>.</p>
		<p>Look around for the best deal, but make sure that the supplier you choose can deliver on its promises - a cheap supplier may not be very responsive and end up costing you more money, while a more expensive supplier may be cheaper in the long run if they can fix problems within hours rather than days.</p>
		
