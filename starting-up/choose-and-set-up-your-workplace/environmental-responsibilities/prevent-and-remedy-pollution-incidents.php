        <h1>Environmental responsibilities</h1>
        <h2>Prevent and remedy pollution incidents</h2>
        <p>Every year there are thousands of cases of damage to the environment. The <strong>Environmental Damage Regulations</strong> relate to the most serious cases, covering:</p>
        <ul>
            <li>damage to species and habitats</li>
            <li>damage to water</li>
            <li>risks to human health from contamination of land</li>
        </ul>
        <p>The regulations apply to both actual cases of damage and threats of imminent damage. If you are responsible - ie you are the 'operator' of the activity that causes or threatens the damage - you must take immediate action to prevent or remedy this.</p>
        <p>See our guide on <a href="layer46a9.html?topicId=1086885096" title="Avoid causing environmental damage from pollution incidents or firefighting, and writing a pollution incident response plan">pollution incidents and environmental damage - an overview</a>.</p>
        <p>
            <h3>Reporting an incident</h3>
            <p>You should call the Environment Agency Incident Hotline on Tel 0800 80 70 60 to report incidents such as:</p>
            <ul>
                <li>damage or danger to the natural environment</li>
                <li>pollution to water or land</li>
                <li>poaching or illegal fishing</li>
                <li>fish in distress or dead fish</li>
                <li>watercourse blocked by a vehicle or fallen tree causing risk of flooding</li>
                <li>illegal dumping of hazardous waste or large amounts of industrial waste</li>
                <li>incidents at Environment Agency-regulated waste sites</li>
                <li>illegal abstraction from watercourses</li>
                <li>unusual drops in river flow</li>
                <li>collapsed or badly damaged river or canal banks</li>
            </ul>
            <p>You should report incidents such as gas leaks, burst water mains, or domestic odours, noise, waste or pest nuisances to your local authority or utility company.</p>
        </p>
