        <h1>Environmental responsibilities</h1>
        <h2>Who enforces environmental legislation?</h2>
        <p>There are three main environmental regulators in the UK:</p>
        <ul>
            <li>the Environment Agency in England and Wales</li>
            <li>the Northern Ireland Environment Agency</li>
            <li>the Scottish Environment Protection Agency</li>
        </ul>
        <p>The environmental regulators have produced the NetRegs website, which provides free environmental guidance. You can <a href="http://www.environment-agency.gov.uk/netregs/" target="bgExternalonlinebusinesslinkgovuk" title="Find out how to manage the different ways your business activities could affect the environment">find guidance on environmental legislation and good practice on the NetRegs website</a>.</p>
        <p>You have the right to appeal against environmental regulators' decisions.</p>
        <p>Your local authority is also responsible for regulating some environmental issues such as:</p>
        <ul>
            <li>air quality</li>
            <li>statutory nuisance problems such as air pollution, noise, odour and light pollution</li>
            <li>land contamination</li>
            <li>environmental health</li>
        </ul>
        <p>See our guide on <a href="layer837b.html?topicId=1074439576" title="Local council services that could affect your business - business rates, trading standards, parking and planning">local authority services: an overview</a>.</p>
