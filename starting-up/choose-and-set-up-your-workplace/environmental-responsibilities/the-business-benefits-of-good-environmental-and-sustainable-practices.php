        <h1>Environmental responsibilities</h1>
        <h2>The business benefits of good environmental and sustainable practices</h2>
        <p>Complying with legislation and going further to adopt environmental and sustainable practices can benefit your business. For example:</p>
        <ul>
            <li>Reducing your energy use can save you money. See our guide on how to <a href="layercbab.html?topicId=1079432186" title="Reduce costs and increase your competitiveness by managing how your business uses electricity, gas and other fuels">save money by using energy more efficiently</a>.</li>
            <li>Reducing your use of raw materials can cut your costs, minimise your waste and lower your waste disposal bills. See our guide on how to <a href="layer33e4.html?topicId=1081267120" title="Your options for reducing and dealing with business waste and the benefits of choosing more sustainable practices">save money by reducing, reusing and recycling waste</a>.</li>
            <li>Reducing your water use can cut your water bills and also lower your waste water disposal costs. See our guide on how to <a href="layer1bd4.html?topicId=1080452940" title="How to use water efficiently, the benefits of saving water and your responsibilities if you take or store water">use water efficiently - the basics</a>.</li>
            <li>Recycling equipment and materials could make you money and save on disposal costs - you may be able to sell old equipment or parts to subsidise the cost of buying new ones. Read our guide on how to <a href="layer33e4.html?topicId=1081267120" title="Your options for reducing and dealing with business waste and the benefits of choosing more sustainable practices">save money by reducing, reusing and recycling waste</a>.</li>
            <li>Taking steps to prevent environmental damage will reduce your risk of incurring costs and of having to carry out potentially expensive remedial work in the event of an environmental incident. You can <a href="http://www.defra.gov.uk/environment/quality/environmental-liability/" target="bgExternalonlinebusinesslinkgovuk" title="Introduction to environmental liability legislation and downloadable guidance">read information and guidance on environmental liability on the Department for Environment, Food and Rural Affairs (Defra) website</a>.</li>
        </ul>
        <p>Your business could also enjoy other benefits. Many government organisations and large companies will only work with businesses who take their environmental responsibilities seriously. You may also find it easier to attract investment from banks and other sources of finance.</p>
        <p><a href="layer3ac4-2.html?topicId=1079438165" title="These interactive tools will help you find out what you need to do to comply with environmental legislation">Use our interactive tool to assess your environmental compliance</a>. You can also <a href="http://online.businesslink.gov.uk/bdotg/action/logicToolStart?itemId=1079446499&amp;type=BLTTOOL" title="Find out how to save money by going green">use our interactive tool to identify where you can save money by going green</a>.</p>
        <p><strong>Sustainable development</strong> can create opportunities for your business. As well as making financial savings, you can enhance your reputation and brand value by fostering customer loyalty and motivating staff. You can also use sustainable development as an opportunity to encourage innovation, increase investment and open new markets.</p>
        <p>Read our guide about the <a href="layer2570.html?topicId=1079416602" title="How a more environmentally friendly approach can protect your business, save money and improve its reputation">benefits of improving your environmental performance</a>.</p>
