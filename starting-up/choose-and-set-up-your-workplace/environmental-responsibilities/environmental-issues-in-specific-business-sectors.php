        <h1>Environmental responsibilities</h1>
        <h2>Environmental issues in specific business sectors</h2>
        <p>Different types of businesses face their own particular environmental issues. For example, factory processes may use large amounts of energy or involve hazardous substances, while office-based practices are more concerned with reducing energy and water waste. Transport businesses may use large amounts of fuel and create significant vehicle emissions.</p>
        <p>Whatever sector you are in, it's a good idea to seek advice on the major issues. <a href="http://www.environment-agency.gov.uk/netregs/" target="bgExternalonlinebusinesslinkgovuk" title="Environmental guidance for small and medium-sized businesses">Find guidance on environmental legislation and good practice for your business on the NetRegs website</a>.</p>
        <p>A good first step is to conduct an environmental review of your business. This will help you to identify what you need to do to comply with the law and how you could minimise waste, emissions, energy usage and your impact on the environment. See our guide on how to <a href="layerfcc5.html?topicId=1082899305" title="How reviewing your business' activities and processes can help you to reduce your environmental impact">carry out an environment review of your business</a>. You can also use our interactive tool to assess your environmental compliance.</p>
        <p>If you would like more specialist advice, you may need to engage an environmental consultant. In certain business sectors, such as quarrying, engineering and chemicals, consultants are regularly used. An environmental consultant can give specific advice about your environmental responsibilities, including insurance and business development plans. See our guide on <a href="layer959f.html?topicId=1079422061" title="How environmental consultants can provide help and advice to your business on environmental issues">using an environmental consultant</a>.</p>
