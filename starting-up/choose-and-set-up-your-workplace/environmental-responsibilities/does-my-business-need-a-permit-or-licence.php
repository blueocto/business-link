        <h1>Environmental responsibilities</h1>
        <h2>Does my business need a permit or licence?</h2>
        <p>You may require a permit or licence if your business poses certain types of environmental risk. For example if your business:</p>
        <ul>
            <li><strong>collects or transports waste</strong> on behalf of others, or transports your own construction or demolition waste, you must register as a waste carrier with your environmental regulator - this is the Environment Agency in England and Wales,&nbsp;the Northern Ireland Environment Agency (NIEA)&nbsp;or&nbsp;the Scottish Environment Protection Agency (SEPA)</li>
            <li>carries out <strong>waste activities</strong> such as treating, storing or disposing of other people's waste, you may need an&nbsp;environmental permit or an exemption from your environmental regulator</li>
            <li>carries out <strong>industrial, waste or intensive agricultural activities</strong>, or uses hazardous substances such as solvents, you may need an environmental permit or a pollution prevention and control permit from your local authority or environmental regulator</li>
            <li><strong>allows anything other than</strong> clean, uncontaminated <strong>water</strong> from your premises into watercourses or surface water drains, you must have a discharge consent from your environmental regulator</li>
            <li>discharges <strong>trade effluent</strong> into the sewerage system, you must get consent from your water company or water authority</li>
            <li>takes water from surface water or groundwater, ie not from the mains supply, you may need an abstraction licence from your environmental regulator</li>
        </ul>
        <p><strong>This list is not comprehensive</strong>. For more information on registering as a waste carrier, see our guide on <a href="layerf48f.html?topicId=1080480296" title="How to comply with the law by getting the right environmental permits and other licences">environmental permits and licences - an overview</a>.&nbsp;You can also <a href="layer3ac4-2.html?topicId=1079438165" title="These interactive tools will help you find out what you need to do to comply with environmental legislation">use our interactive tool to assess your environmental compliance</a>.&nbsp;</p>
        <p>You can contact the Environment Agency Helpline on Tel 03708 506 506.</p>
        <p>Businesses in Northern Ireland should contact the NIEA Helpline on Tel 0845 302 0008.</p>
        <p>Businesses in Scotland should contact SEPA on Tel 01786 457 700.</p>
