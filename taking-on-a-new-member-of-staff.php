				<h1>Taking on a new member of staff?</h1>
				<h2>Use our new and improved interactive tools</h2>
				<p>We have launched three new tools to help you through the process of taking on a new member of staff.</p>
				<p>You can find the new tools on <a href="layer9c2b.html?topicId=1081476661" title="Our interactive tool can help you comply with the law when employing new staff" >a single page within the Employment and skills section of the site</a>. They deal separately with:</p>
				<ul>
				<li><a href="http://online.businesslink.gov.uk/bdotg/action/logicToolStart?itemId=1087845833&amp;type=BLTTOOL" title="The steps you should take when looking to hire new staff" >recruiting staff</a></li>
				<li><a href="http://online.businesslink.gov.uk/bdotg/action/logicToolStart?itemId=1087958695&amp;type=BLTTOOL" title="Making pre-employment checks tool" >making pre-employment checks and contracts of employment</a></li>
				<li><a href="http://online.businesslink.gov.uk/bdotg/action/logicToolStart?itemId=1088043132&amp;type=BLTTOOL" title="Getting new workers started and contracts of employment" >getting new workers started</a></li>
			</ul>
				<p>Previously, a single tool attempted to cover all three stages of this process. We listened to your feedback about this tool, which told us that it contained too many questions and took too long to work through.</p>
				<p>You also told us that the process of taking on a new member of staff could be broken down into smaller, separate stages. You wanted to know your obligations at each stage separately, without having to sift through information about the other stages at the same time.</p>
				<p>The three shorter tools that we have developed will provide just the information that you need, when you need it.</p>
				<p>A related tool for when you're <a href="layer37d7.html?topicId=1086156452" title="Interactive tool to help an employer go through the steps of paying someone for the first time" >paying an employee for the first time</a> can also be found at the single page within the Employment and skills section of the site.</p>
