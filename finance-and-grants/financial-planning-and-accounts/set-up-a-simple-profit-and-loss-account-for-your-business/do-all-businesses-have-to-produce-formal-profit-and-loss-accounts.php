		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Do all businesses have to produce formal profit and loss accounts?</h2>
		<p>By law, if your business is a <strong>limited company</strong> or a <strong>partnership whose members are limited companies</strong>, you must produce a profit and loss account for each financial year.</p>
		<p>Self-employed sole traders and most partnerships don't need to create a formal profit and loss account - but they do need to keep adequate records to complete their Self Assessment tax return fully and accurately.</p>
		<p>However, there are key benefits to producing formal accounts. If you are looking to grow your business, or need a loan or mortgage, for example, most institutions will ask to see three years' accounts.</p>
		
