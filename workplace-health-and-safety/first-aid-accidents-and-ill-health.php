<h1>First aid, accidents and ill health</h1>
<h2>Introduction</h2>
<img src="../../_img/health_safety.gif" width="94" height="62" border="0" alt="" />
<p>You must have first aid arrangements in your workplace, as you are responsible for making sure that your employees receive immediate attention if they are taken ill or are injured at work.</p>
<p>Under health and safety law, you must also report and keep a record of certain injuries, incidents and cases of work-related disease.</p>
<p>This guide gives an overview of first aid provision, which incidents you must report and how records should be kept.</p>

