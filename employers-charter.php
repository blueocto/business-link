				<h1>Employer's Charter</h1>
				<h2>Find out what you can and can't do when managing your staff</h2>
				<p>For employment law to be effective there needs to be a balance between protecting employees and enabling businesses to operate effectively in order to support economic growth.</p>
				<p>Advice and guidance on employment law often focuses on the rights of employees and what employers have to do to comply with their responsibilities. This focus can leave some businesses feeling they have lots of responsibilities but no rights, the balance is all in the favour of the employee, and being afraid to take on and effectively manage their staff.</p>
				<p>The <strong>Employer's Charter</strong> aims to dismiss some of the myths about what employers can and can't do in managing their workforce. It tells you what you reasonably can ask and know about your employees, and what action you can take if there are problems.</p>
				<h3>The Employer's Charter</h3>
				<p>As an employer -as long as you act fairly andreasonably -you can:</p>
				<ul>
				<li>contact a woman on maternity leave and ask when she plans to return</li>
				<li>reject an employee's request to work flexibly if you have a legitimate business reason</li>
				<li>talk to your employees about their performance and how they can improve</li>
				<li>dismiss an employee for poor performance</li>
				<li>withhold pay from an employee when they are on strike</li>
				<li>require an employee to take their holiday at a time that suits your business</li>
				<li>stop providing work to an agency worker (as long as they are not employed by you)</li>
				<li>make an employee redundant if your business takes a downward turn</li>
				<li>seek an independent assessment of your employee's fitness for work</li>
				<li>dismiss an employee if they have had a period of long-term absence or repeated short-term absences</li>
			</ul>
				<p>You can also ask an employee:</p>
				<ul>
				<li>to take a pay cut or change some other terms and conditions, for example their normal working hours</li>
				<li>whether they would be willing to opt out from the 48-hour limit in the Working Time Regulations</li>
				<li>about their future career plans, including retirement</li>
			</ul>
				<p>And:</p>
				<ul>
				<li>during interview you can ask a job candidate questions that are relevant to the requirements of the job (but questions need to be fair and unbiased)</li>
				<li>to settle a dispute, you can protect yourself against most claims from an employee by entering into a 'compromise agreement' (often in return for an agreed payment)</li>
			</ul>
				<p>This is intended to help employers understand what they can do in general. Of course, individual circumstances may vary and employers should act in accordance with their legal obligations.</p>
