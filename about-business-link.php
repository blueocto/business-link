								<div>
									<p>Business Link is government's online resource for businesses.</p>
									<p>It contains essential information, support and services for you and your business &ndash; whether you work for a large organisation or are on your way to starting up.</p>
									<p>Simple to use, up to date and practical, Business Link is the first place to go to find guidance on regulations and to access government services. It also has a number of useful online tools, calculators, and best practice case studies; and provides access to funding options, as well as wider support.</p>
									<p><strong>Business link is changing</strong></p>
									<p>From October GOV.UK will be the new home for government services and information. GOV.UK will replace businesslink.gov.uk and Directgov, making it simpler, clearer and faster to find what you need from government. <a href="gov-uk-will-launch-in-october.php">Find out how businesslink.gov.uk is changing</a>. </p>
