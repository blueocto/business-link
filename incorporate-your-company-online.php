				<h1>Incorporate your company online</h1>
				<p>This service enables you to incorporate your company online as a private company, limited by shares, with model articles of association.</p>
				<p>For explanations of company types and how to incorporate, see our guide on <a href="layer4919.html?topicId=1073865730" title="Company, partnership or sole trader - find the right structure for your new business" >legal structures: the basics</a>.</p>
				<p>To use the service, you will need to register with your email address and a password of your choice.</p>
				<p>You will then need the following information to complete the form:</p>
				<ol>
							<li>Company name and address</li>
							<li>Details of directors and secretary (optional)</li>
							<li>Details of each shareholder and the value of their shares</li>
							<li>Payment details -<a href="http://www.companieshouse.gov.uk/toolsToHelp/ourPrices.shtml" target="bgExternalonlinebusinesslinkgovuk" onclick="p=window.open('http://www.companieshouse.gov.uk/toolsToHelp/ourPrices.shtml', 'bgExternalonlinebusinesslinkgovuk');p.focus();" title="Current prices for Companies House services" >find out more about Companies House fees on the Companies House website<span> - Opens in a new window</span></a> </li>
						</ol>
