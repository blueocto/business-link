				<h1>File company information online using Companies House WebFiling</h1>
				<h3>Before you start</h3>
				<p>WebFiling allows you to file most information online free of charge. <a href="http://www.companieshouse.gov.uk/toolsToHelp/ourPrices.shtml" target="bgExternalonlinebusinesslinkgovuk" title="Current prices for Companies House services">Find out more about Companies House fees on the Companies House website<span> - Opens in a new window</span></a>.</p>
				<p>All major credit/debit cards and PayPal are accepted. MasterCard SecureCode and Verified by Visa are also used to improve online card payment security. <a href="https://ewf.companieshouse.gov.uk/help/en/stdwf/doclist06.html" target="bgExternalonlinebusinesslinkgovuk" title="WebFiling forms and exceptions">Find out which forms can be filed online and a list of filing exceptions<span> - Opens in a new window</span></a>.</p>
				<p>This service is delivered by Companies House in conjunction with businesslink.gov.uk. To start, you will need to complete the registration process as follows:</p>
				<ol>
					<li>provide a valid email address</li>
					<li>wait for an email with our security code</li>
					<li>apply for an authentication code for each company you intend to file information on to be posted to your registered address</li>
				</ol>
				<p>Each time you use WebFiling, log in and enter the company number and authentication code.</p>
				<p>Any data captured through using WebFiling is stored with the Registrar of Companies and not businesslink.gov.uk and is subject to the Companies House privacy policy. <a href="http://www.companieshouse.gov.uk/legal/privacy.shtml" target="bgExternalonlinebusinesslinkgovuk" title="Privacy Statement from Companies House">Read the Companies House privacy policy on the Companies House website<span> - Opens in a new window</span></a>.</p>
				<div>
					<a href="https://ewf.companieshouse.gov.uk/seclogin" target="bgExternalonlinebusinesslinkgovuk" title="Access the online WebFiling service provided by Companies House">Sign in to Companies House WebFiling<span> - Opens in a new window</span></a>
				</div>
			</p>
