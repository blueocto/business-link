		<h1>Protecting intellectual property</h1>
		<h2>Introduction</h2>
		<img src="/_img/GI0800.gif" width="78" height="118" border="0" alt="" />
		<p>All businesses have intellectual property (IP), regardless of their size or sector. IP can mean a brand, invention, design or other kind of creation, and it can be legally owned.</p>
		<p>Your IP is likely to be a valuable asset. It could include the name of your business, the products or services you make or provide, or the written or artistic material you create.</p>
		<p>Securing and protecting it could be essential to your business' future success, so it's vital to understand your rights and how the law can help you.</p>
		<p>This guide explains the importance of conducting an audit of your business' IP. It sets out the different kinds of legal protection available for IP available (including patents, trade marks, design rights, registered designs and copyright) and explains the range of things you can do to protect and manage your IP rights.</p>
		
