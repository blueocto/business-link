            <h1>Applications for trade union recognition</h1>
            <h2>Introduction</h2>
            <img src="/_img/P45.gif" width="78" height="118" alt="" />
            <p><strong>Recognition</strong> means that a trade union can conduct collective bargaining on behalf of your employees.</p>
            <p>This interactive tool helps you understand your choices in the application process. You should use this when either of the following apply:</p>
            <ul type="disc">
                <li>when a trade union formally applies to you for recognition</li>
                <li>when you want to voluntarily recognise a trade union, without the trade union formally applying to you</li>
            </ul>
            <p>This tool is not a substitute for professional advice. You may also want to:</p>
            <ul type="disc">
                <li>discuss the options informally with a trade union official - this does not commit either party to any particular solution</li>
                <li>take advice from a range of professional and trade organisations including Acas </li>
                <li>get impartial information about recognising a trade union on the Central Arbitration Committee (CAC) website</li>
            </ul>
