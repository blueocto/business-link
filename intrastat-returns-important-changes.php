				<h1>Intrastat returns: important changes</h1>
				<h2>Submitting Intrastat declarations from 1 April 2012: a reminder</h2>
				<h3>Who needs to read this?</h3>
				<p>All businesses that provide Intrastat returns to HMRC Trade Statistics.</p>
				<h3>What will businesses need to do from 1 April 2012?</h3>
				<ul>
				<li>Submit your March 2012 return by <strong>21 April 2012</strong>. From then on, all Intrastat Supplementary Declarations must be made by the 21st day of the month.</li>
				<li>Provide all Intrastat Supplementary Declarations using electronic methods, eg online form, Comma Separated Value (CSV) file format or Electronic Data Interchange (EDI).</li>
			</ul>
				<h3>Where can I get more information?</h3>
				<p>You can<a href="https://www.uktradeinfo.com/Intrastat/Pages/Intrastat.aspx" target="bgExternalonlinebusinesslinkgovuk" title="How to get started with making Intrastat declarations online" >find out more about making Intrastat declarations on the uktradeinfo website.<span> - Opens in a new window</span></a></p>
				<p>You can also contact the HMRC Customer Service Helpdesk on <a href="mailto:Intrastatonline@hmrc.gsi.gov.uk"><u>Intrastatonline@hmrc.gsi.gov.uk</u></a> quoting <strong>'Intrastat 2012'</strong> or telephone 01702 367248.</p>
